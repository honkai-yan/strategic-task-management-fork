// 生成里程碑（历史数据里程碑大部分已完成）
const generateHistoricalMilestones = (year: number, progress: number): Milestone[] => {
  const milestones: Milestone[] = []
  for (let q = 1; q <= 4; q++) {
    const targetProgress = q * 25
    const deadline = `${year}-${String(q * 3).padStart(2, '0')}-${q === 1 ? '31' : q === 2 ? '30' : q === 3 ? '30' : '31'}`
    const status: 'completed' | 'pending' | 'overdue' = progress >= targetProgress ? 'completed' : 'pending'
    milestones.push({
      id: `hist-${year}-${q}`,
      name: `Q${q}: 阶段目标`,
      targetProgress,
      deadline,
      status
    })
  }
  return milestones
}

